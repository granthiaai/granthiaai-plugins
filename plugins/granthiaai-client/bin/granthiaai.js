function openBrowser(url) {
  let cmd;
  let args;
  switch (platform()) {
    case "darwin":
      cmd = "open";
      args = [url];
      break;
    case "win32":
      cmd = "rundll32";
      args = ["url.dll,FileProtocolHandler", url];
      break;
    default:
      cmd = "xdg-open";
      args = [url];
  }
  try {
    spawn(cmd, args, { stdio: "ignore", detached: true }).on("error", () => {
    }).unref();
  } catch {
  }
}
