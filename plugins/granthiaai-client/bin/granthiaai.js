var USAGE = `Granthia CLI

Usage:
  granthiaai login    Authorize background sync (opens the browser; --headless for device flow)
  granthiaai logout   Remove cached credentials
  granthiaai status   Show login state, engine URL, and last sync
  granthiaai sync     Sync sessions (Stop hook = targeted; manual = full scan)`;
async function readHookPayload() {
  if (process.stdin.isTTY) return null;
  const chunks = [];
  for await (const chunk of process.stdin) chunks.push(chunk);
  const raw = Buffer.concat(chunks).toString("utf-8").trim();
  if (!raw) return null;
  try {
    return JSON.parse(raw);
  } catch {
    return null;
  }
}
async function main() {
  const command = process.argv[2];
  switch (command) {
    case "login":
      await loginCommand({ headless: process.argv.includes("--headless") });
      return;
    case "logout":
      await logoutCommand();
      return;
    case "status":
      await statusCommand();
      return;
    case "sync": {
      try {
        await runSync(await readHookPayload());
      } catch (err) {
        await appendLog(`sync error: ${err.message}`);
      }
      return;
    }
    default:
      console.log(USAGE);
      return;
  }
}
main().catch((err) => {
  console.error(err.message);
  process.exitCode = 1;
});
